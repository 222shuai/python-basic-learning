#DrawPython.py
import turtle
import time

turtle.setup(1000, 800, 200, 50)
turtle.penup()
turtle.fd(-250)
turtle.pendown()
turtle.pensize(15)
turtle.pencolor("purple")
turtle.seth(-40)
for i in range(2):
    turtle.circle(40, 80)
    turtle.circle(-40, 80)
turtle.circle(40, 80/2)
turtle.fd(40)
turtle.circle(30, 480)
turtle.color("yellow")
for i in range(3):
    turtle.circle(40, 80)
    turtle.circle(-40, 80)
turtle.circle(40, 80/2)
turtle.fd(40)
turtle.circle(30, 400)
turtle.fd(40 * 2/3)
turtle.color("red")
for i in range(2):
    turtle.circle(40, 80)
    turtle.circle(-40, 80)
turtle.circle(40, 80/2)
turtle.fd(40)
turtle.circle(30, 410)
turtle.color("blue")
for i in range(3):
    turtle.circle(40, 80)
    turtle.circle(-40, 80)
turtle.circle(40, 80/2)
turtle.fd(40)
turtle.circle(30, 400)
turtle.fd(40 * 2/3)
turtle.color("orange")
for i in range(2):
    turtle.circle(40, 80)
    turtle.circle(-40, 80)
turtle.circle(40, 80/2)
turtle.fd(40)
turtle.circle(30, 300)
turtle.fd(40 * 2/3)
turtle.color("pink")
for i in range(3):
    turtle.circle(40, 80)
    turtle.circle(-40, 80)
turtle.circle(40, 80/2)
turtle.fd(40)
turtle.circle(30, 150)
turtle.fd(40 * 2/3)
time.sleep(2)

turtle.penup()
turtle.goto(200,100)
turtle.showturtle()
turtle.color("red")
turtle.write("Hello!", font=('Arial', 40, 'normal'))
time.sleep(1)
